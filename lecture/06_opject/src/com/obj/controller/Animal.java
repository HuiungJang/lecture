package com.obj.controller;

public class Animal {
	public String jong;
	public String name;
	public int age;
	public String home;
	public int leg;
	public char gender;
	
	public Animal(String jong,String name, int age,
					String home, int leg, char gender){
		this.jong = jong;
		this.name = name;
		this.age = age;
		this.home = home;
		this.leg = leg;
		this.gender =gender;
	}
}
